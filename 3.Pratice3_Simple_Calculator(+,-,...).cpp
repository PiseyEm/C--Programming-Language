#include<iostream>
using namespace std;
int main(){
	//declare Variable 
	float num1,num2; 
	cout<<"========================"<<endl; 
	//Input Number1&2
	cout<<"Enter Number1= "; cin>>num1;
	cout<<"Enter Number2= "; cin>>num2;
	cout<<"========================"<<endl;
	//Output + - * /
	cout<<num1<<"+"<<num2<<"="<<(num1+num2)<<endl;
	cout<<num1<<"-"<<num2<<"="<<(num1-num2) <<endl;
	cout<<num1<<"*"<<num2<<"="<<(num1*num2)<<endl;
	cout<<num1<<"/"<<num2<<"="<<(num1/num2); 
	return 0;
}        
