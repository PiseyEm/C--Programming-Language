#include<iostream>
#include<math.h>
using namespace std;
int main(){
	//Find Log
	float a,y;
	cout<<"Input a="; cin>>a;
	y=log10(a);
	cout<<"log10("<<a<<")="<<y<<endl;
	//Find Sin
	float x;
	cout<<"Input x="; cin>>x;
	y=sin(x);
cout<<"Sin"<<x<<"="<<y;
	cout<<y;
	//Find Cos
	float x1;
	cout<<"Input x="; cin>>x1;
	y=cos(x1);
	cout<<"Cos"<<x1<<"="<<y;
	cout<<y;
	return 0;
	
}
